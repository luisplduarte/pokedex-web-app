"use client";

import { useEffect } from "react";

/** Registers the service worker for offline navigation cache. */
export function ServiceWorkerRegister() {
  useEffect(() => {
    if (
      typeof window === "undefined" ||
      !("serviceWorker" in navigator)
    ) return;

    navigator.serviceWorker
      .register("/sw.js")
      .catch(() => {});
  }, []);

  return null;
}
